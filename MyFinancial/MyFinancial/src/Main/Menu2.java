    private void cariLaporanBudgetingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariLaporanBudgetingActionPerformed
        String SUrl = "jdbc:MySQL://localhost:3306/myfinancial";
        String SUser = "root";
        String SPass = "";
        DefaultTableModel model = (DefaultTableModel) laporanBudgetingTable.getModel(); 

        model.setRowCount(0);

        int bulan = bulanLaporanBudgeting.getMonth() + 1; 
        int tahun = tahunLaporanBudgeting.getYear();

        if (bulan <= 0 || tahun <= 0) {
            JOptionPane.showMessageDialog(null, "Harap pilih bulan dan tahun!");
            return;
        }

        String namaBulan = getNamaBulan(bulan); 

        try {
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            String query = "SELECT bulan, tahun, kategori, jumlah FROM budgeting WHERE id_user = ? AND bulan = ? AND tahun = ?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setInt(1, idUser);
            ps.setString(2, namaBulan);
            ps.setInt(3, tahun);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String bulanResult = rs.getString("bulan");
                int tahunResult = rs.getInt("tahun");
                String kategori = rs.getString("kategori");
                double jumlah = rs.getDouble("jumlah");

                DecimalFormat decimalFormat = new DecimalFormat("#,##0.00");
                String jumlahFormatted = decimalFormat.format(jumlah);

                model.addRow(new Object[]{bulanResult, tahunResult, kategori, jumlahFormatted});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error! " + e.getMessage());
        }
    }//GEN-LAST:event_cariLaporanBudgetingActionPerformed

