    private void hitungTotalLaporanBudgetingBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungTotalLaporanBudgetingBtnActionPerformed
        double total = 0.0;

        for (int i = 0; i < laporanBudgetingTable.getRowCount(); i++) {
            Object jumlahObj = laporanBudgetingTable.getValueAt(i, 3);
            if (jumlahObj != null && !jumlahObj.toString().isEmpty()) {
                try {
                    String jumlahStr = jumlahObj.toString().replace(".", "").replace(",", ".");
                    total += Double.parseDouble(jumlahStr);
                } catch (NumberFormatException e) {
                    System.out.println("Error parsing number: " + jumlahObj.toString());
                    e.printStackTrace();
                }
            }
        }
        DecimalFormat decimalFormat = new DecimalFormat("#,##0.00");
        totalLaporanBudgetingTF.setText(decimalFormat.format(total)); 
    }//GEN-LAST:event_hitungTotalLaporanBudgetingBtnActionPerformed

    private void cetakLaporanBudgetingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakLaporanBudgetingActionPerformed
                if (laporanBudgetingTable.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tidak ada data untuk dicetak!", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        for (int column = 0; column < laporanBudgetingTable.getColumnCount(); column++) {
            int width = 0;
            for (int row = 0; row < laporanBudgetingTable.getRowCount(); row++) {
                TableCellRenderer renderer = laporanBudgetingTable.getCellRenderer(row, column);
                Component comp = laporanBudgetingTable.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width, width);
            }

            TableColumn col = laporanBudgetingTable.getColumnModel().getColumn(column);
            col.setPreferredWidth(width);
        }

        laporanBudgetingTable.setFont(new Font("SansSerif", Font.PLAIN, 14)); 
        laporanBudgetingTable.getTableHeader().setFont(new Font("SansSerif", Font.BOLD, 16)); 
        MessageFormat header = new MessageFormat("Laporan Budgeting\n\n\n\n\n\n\n\n\n\n");
        MessageFormat footer = new MessageFormat("MyFinancial: Personal Finance Tracker Version 1.0.0 "
                + "| Page {0,number,integer}");

        try {
            boolean printed = laporanBudgetingTable.print(JTable.PrintMode.FIT_WIDTH, header, footer);
            if (printed) {
                JOptionPane.showMessageDialog(this, "Laporan berhasil dicetak!", "Sukses", 
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Pencetakan dibatalkan.", "Info", 
                        JOptionPane.WARNING_MESSAGE);
            }
        } catch (PrinterException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mencetak: " 
                    + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cetakLaporanBudgetingActionPerformed

