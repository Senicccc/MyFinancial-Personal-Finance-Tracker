    private void cetakLaporanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakLaporanButtonActionPerformed
        if (laporanTable.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tidak ada data untuk dicetak!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        for (int column = 0; column < laporanTable.getColumnCount(); column++) {
            int width = 0;
            for (int row = 0; row < laporanTable.getRowCount(); row++) {
                TableCellRenderer renderer = laporanTable.getCellRenderer(row, column);
                Component comp = laporanTable.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width, width);
            }

            TableColumn col = laporanTable.getColumnModel().getColumn(column);
            col.setPreferredWidth(width);
        }

        laporanTable.setFont(new Font("SansSerif", Font.PLAIN, 14)); 
        laporanTable.getTableHeader().setFont(new Font("SansSerif", Font.BOLD, 16)); 
        MessageFormat header = new MessageFormat("Laporan Budgeting\n\n\n\n\n\n\n\n\n\n");
        MessageFormat footer = new MessageFormat("MyFinancial: Personal Finance Tracker Version 1.0.0 "
                + "| Page {0,number,integer}");

        try {
            boolean printed = laporanTable.print(JTable.PrintMode.FIT_WIDTH, header, footer);
            if (printed) {
                JOptionPane.showMessageDialog(this, "Laporan berhasil dicetak!", "Sukses", 
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Pencetakan dibatalkan.", "Info", 
                        JOptionPane.WARNING_MESSAGE);
            }
        } catch (PrinterException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mencetak: " 
                    + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cetakLaporanButtonActionPerformed

