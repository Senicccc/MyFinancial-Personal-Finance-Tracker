    public void rekapPendapatanPengeluaranBudget(String interval) {
        String SUrl = "jdbc:MySQL://localhost:3306/myfinancial";
        String SUser = "root";
        String SPass = "";

        try (Connection con = DriverManager.getConnection(SUrl, SUser, SPass)) {
            String pendapatanQuery = "SELECT SUM(jumlah) AS pendapatan FROM pendapatan WHERE id_user = ? AND tanggal >= CURDATE() - INTERVAL ? MONTH";
            String pengeluaranQuery = "SELECT SUM(jumlah) AS pengeluaran FROM pengeluaran WHERE id_user = ? AND tanggal >= CURDATE() - INTERVAL ? MONTH";
            String budgetingQuery = "SELECT SUM(jumlah) AS budget FROM budgeting WHERE id_user = ? AND tahun = YEAR(CURDATE()) AND bulan = MONTH(CURDATE())";

            PreparedStatement psPendapatan = con.prepareStatement(pendapatanQuery);
            psPendapatan.setInt(1, idUser);
            psPendapatan.setString(2, interval); 
            ResultSet rsPendapatan = psPendapatan.executeQuery();

            double totalPendapatan = rsPendapatan.next() ? rsPendapatan.getDouble("pendapatan") : 0;
            pendapatanNum.setText(String.format("%.2f", totalPendapatan));

            PreparedStatement psPengeluaran = con.prepareStatement(pengeluaranQuery);
            psPengeluaran.setInt(1, idUser);
            psPengeluaran.setString(2, interval); 
            ResultSet rsPengeluaran = psPengeluaran.executeQuery();

            double totalPengeluaran = rsPengeluaran.next() ? rsPengeluaran.getDouble("pengeluaran") : 0;
            pengeluaranNum.setText(String.format("%.2f", totalPengeluaran));

            PreparedStatement psBudget = con.prepareStatement(budgetingQuery);
            psBudget.setInt(1, idUser);
            ResultSet rsBudget = psBudget.executeQuery();

            double totalBudget = rsBudget.next() ? rsBudget.getDouble("budget") : 0;
            budgetNum.setText(String.format("%.2f", totalBudget));

            double selisih = totalPendapatan - totalPengeluaran;
            selisihNum.setText(String.format("%.2f", selisih));

            rsPendapatan.close();
            rsPengeluaran.close();
            rsBudget.close();
            psPendapatan.close();
            psPengeluaran.close();
            psBudget.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error saat mengambil data: " + e.getMessage());
           
    }
}
    
